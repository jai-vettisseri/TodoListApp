using TodoListApp.Server;

var builder = WebApplication.CreateBuilder(args);

// Register services for the web application (infrastructure, application, controllers, OpenAPI)
builder = builder.AddWebServices();

var app = builder.Build();

app.UseDefaultFiles();
app.MapStaticAssets();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.MapOpenApi();
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.MapFallbackToFile("/index.html");

app.Run();
